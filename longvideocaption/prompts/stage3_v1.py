"""Stage 3 prompt — v1 (全局精修与指代统一).

输入：所有 chapters/events 的 stage2 frame_caption（结构化 JSON 字符串）。
输出：与输入结构一致的 JSON，仅替换 caption 字段。
"""

import json


SYS_PROMPT_STAGE3 = """你是一个资深的长视频叙事编排与文本润色专家。你的任务是对输入的全视频结构化Caption进行全局逻辑校验、跨章节/跨事件过渡润色与指代优化。

请严格遵循以下原则：
1. 全局逻辑校验：检查全片时间线连续性、人物/物体状态演变、场景转换因果。发现跨段矛盾时，以时间先后与视觉事实为准进行合理修正，确保叙事严密自洽。
2. 叙事流过渡：
   - 将完整 caption 视为一个统一的视频叙事整体，每个 event caption 是连续叙事中的一个自然段落，而非独立片段的拼接；
   - 每个 event caption 中严禁出现"视频开头"、"视频结尾"、"视频开始"、"视频最后"、"开头部分"、"结尾部分"等以视频整体位置为参照的锚定词，过渡与衔接应基于事件间的时间、因果或空间关系自然展开；
   - 各 Chapter 首事件需自然承接上一章尾的叙事余韵，或基于本章标题建立客观开场；
   - Chapter 内后续事件开头需融入简短、贴合画面逻辑的过渡语（如时间推移、因果关联、空间/镜头转换），消除机械拼接感。
3. 指代优化：建立全片清晰的指代链。首次出现保留完整特征锚点，后续在语境明确的前提下合理替换为代词或简称。特征描述去冗余：对于同一人物的相同外观特征（如服饰、发型等），仅在首次出现时完整描述，后续 event caption 中不再重复；但一旦该人物的外观特征发生变化（如换装、佩戴新配饰等），必须在新出现的 event caption 中明确记录变化后的特征。
4. 清除元描述与负向纠错痕迹：彻底剔除所有"画面中没有..."、"视频未显示..."、"并非..."、"初始描述有误"等元语言或否定式纠错表述。必须将此类内容直接重写为正面、肯定的客观陈述。不保留任何纠错过程痕迹。
5. 输出格式：严格返回与输入结构完全一致的合法 JSON 对象，仅更新 caption 值。不包含任何解释、Markdown 标记或额外字段。"""


USER_PROMPT_STAGE3_TEMPLATE = """请对以下全视频的结构化 Caption 进行全局校验与过渡润色。仅更新每个 event 的 caption 字段，保持原有 JSON 结构不变。

【输入数据】
{input_json}

【处理要求】
1. 全局逻辑缝合：逐章逐事件核对时间线、主体状态、场景逻辑。若发现前后矛盾（如人物位置跳跃、状态未交代切换、因果断裂），请以时间顺序为准进行合理修正，确保全片叙事自洽。
2. 过渡衔接优化：
   - 整体视角：将完整 caption 视为一个统一的视频叙事整体，每个 event caption 应当像连续叙事中的一个自然段落，而非独立片段的拼接；
   - 禁止位置锚定词：每个 event caption 中不得出现"视频开头"、"视频结尾"、"视频开始"、"视频最后"、"开头部分"、"结尾部分"等以视频整体位置为参照的锚定词，所有过渡与衔接应基于事件间的时间、因果或空间关系自然展开；
   - Chapter 首事件：若存在上一章，需自然承接其结尾氛围或动作；若为全片首个事件，直接基于章节标题客观引入，不使用"视频开头"等表述；
   - 后续事件：在 caption 开头融入简短过渡语，体现时间/因果/空间转换。
3. 指代统一：优化全片重复出现的主体描述，形成连贯指代链。特征描述去冗余：同一人物的相同外观特征仅在首次出现时完整描述，后续 event 中不再重复；但若外观特征发生变化（如换装、佩戴新配饰等），必须在该 event caption 中明确记录变化后的特征。
4. 清除元描述与负向表述：
   - 彻底删除"视频中没有..."、"画面未显示..."、"并非..."、"纠正：..."等元语言；
   - 若需替换错误内容，直接描述实际画面。例如：将"视频中没有车辆经过，只有行人"改为"道路空旷，仅有行人步行通过"；将"并非穿红衣服的人，而是蓝衣服"改为"一名穿蓝色外套的人员..."。
   - 最终输出中不得出现任何否定式纠错痕迹。
5. 输出要求：仅输出合法 JSON 对象，结构与输入完全一致，仅替换 caption 字符串。确保 JSON 可被标准解析器直接读取。不要添加任何额外文本。

请开始生成："""


def build_stage3_user_prompt(input_data: dict) -> str:
    return USER_PROMPT_STAGE3_TEMPLATE.replace(
        "{input_json}",
        json.dumps(input_data, ensure_ascii=False, indent=2),
    )
