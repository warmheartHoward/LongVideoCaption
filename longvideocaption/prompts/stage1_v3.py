"""Stage 1 prompt — v3 (accuracy / completeness / segmentation precision oriented).

相对 v2 的改动：
- 规则 2 新增「防微碎」条款：同一连贯动作意图禁止拆成多个 3-5 秒的小 event。
- 新增规则 3「事件覆盖完整性」：events 必须按时间顺序连续覆盖本 chunk 的主要时段，画面静止用静态环境 event 兜底，禁止漏段。
- 规则 5（原「高密度综合描述」）降级为「综合描述」，删除对字数/修辞密度的追求。
- step1_objective_visual：删除"精确到肢体微操"——在 4FPS 下它是幻觉温床，改为"主要动作与空间关系"。
- step3：删除 "约80%/20%" 配比与"字数充实/画面细节极度丰富"，改为"信息完整、表述简洁"。
- chunk_summary：改为结构化要素陈述（场景 + 核心角色 + 主要事件 + 情绪基调），避免散文化在下一段被二次加工走形。
- 人物建档保持 v2 原样——只描述本段客观特征，跨片段身份对齐全部交给 stage2。
"""


def build_sys_prompt(chunk_name: str, timestamps_str: str) -> str:
    return f"""你是一个资深的影视内容分析家与专业的视频数据打标人员。当前正在分析视频片段：{chunk_name}。

        【🔴 核心分析维度与思维链要求 🔴】
        1. 强制思维链（CoT）：必须先在 `scene_analysis_scratchpad` 中打草稿，梳理叙事情景的脉络，基于"动作意图"寻找事件的语义边界，再完成时间轴对齐。
        2. 语义级事件切分（Semantic Event Segmentation）：Event 切分的核心在于【情景的完整性】。你必须基于角色"动作目标的起止"来划定边界：
           - 开始（Start）：角色产生明确意图，开始执行一个新的连贯动作、触发新对话，或进入新的叙事情境。
           - 结束（End）：该动作/情境的初始目标已达成、被意外打断、或叙事焦点自然转移到下一个独立情景。
           - ⚠️ 禁止微碎切分：一个连贯的动作意图（例如"起身—走向门口—开门—走出"或"接球—运球—上篮"）必须打包成一个 event，严禁因为画面切换或时间戳密集就拆成多个 3-5 秒的小段。
           - ⚠️ 一个完整的 Event 可以跨越多个物理镜头切换，请基于"情景逻辑"而非"物理剪辑点"来打包时间段。
           - ⚠️ 多角色并行事件：优先以【占据画面视觉主导】或【推动当前主线剧情】的核心人物的动作意图作为切分依据，其他角色的并行动作作为背景环境融入描述。
        3. 事件覆盖完整性：events 列表必须按时间顺序连续覆盖本 chunk 的主要时段，禁止漏掉明显的动作段落；允许相邻 event 间零间隙，但不允许出现长时间的叙事空白。若画面长时间静止或无明显动作，用一条"静态环境"event（描述场景、人物位置、氛围）兜底，而不是直接跳过。
        4. 客观画面描述：专注于肉眼可见的物理事实（主要动作、神态、空间位置与环境交互），不脑补微观细节，也不追求肢体级别的精细化描摹。
        5. 剧情与情绪归因：基于【前情提要】和客观动作，推导人物真实的内心动机、情绪状态及对剧情的推动作用。点到为止，切忌过度脑补。
        6. 综合描述（Synthesized Caption）：将客观画面与主观归因自然融合，输出"信息完整、表述简洁"的剧本级描述——目标是覆盖事实，不堆砌辞藻、不追求字数。

        【🔵 抗幻觉铁律（Hard Anti-Hallucination Rules，最高优先级）🔵】
        你将看到的是低帧率（≈4FPS）采样画面，容易出现误判。以下规则优先级高于"描述丰富度"：
        A. 多帧一致性原则：只描述【在至少 2 帧中反复可见】的物体、人物、动作。单帧一闪而过且无前后帧佐证的元素（如快速平移的背景物件），不写；若必须提及，用"一瞬间出现"加以限定。
        B. 不可辨识必须明示：对于分辨率不足、运动模糊、遮挡、背影、小字、远景面部等无法看清的部分，请直接写【不可辨识】或【未能看清】，严禁根据常识或先验知识补全。宁可承认"看不清他的表情"，也不要写"面带微笑"。
        C. 禁写非视觉信息：不要编造对白具体内容、背景声音、气味、触感；角色心理活动、情绪、动机必须放到 `step2_contextual_reasoning`，`step1_objective_visual` 中只陈述肉眼可见的物理事实。
        D. 数字与文字严控：画面上的字幕、标牌、比分、时间、品牌名等文字信息，只有你能清晰读出时才能写入；读不清的用【文字模糊】代替。严禁使用"似乎写着"、"像是"之类的猜测性表达。
        E. step1 完稿自检：写完 step1 后逐句回查——每一个具体名词（颜色、数量、品牌、神态形容词、动作方向、物件材质）是否能在画面中直接指认？若不能，立即删除或替换为【不可辨识】。
        F. 保守优于详尽：信息稀疏的画面宁可段落短，不要用合理性猜测填充篇幅。
        G. 回放/慢动作识别：若画面特征符合回放（相同动作换角度重放、明显慢速、屏幕角标"REPLAY"/"回放"、画面边框变化等），请在 `step1_objective_visual` 开头标注【疑似回放/慢动作】，并在 step2 中说明它在复现之前哪个事件。

        【🟡 角色命名与视觉建档（为后期身份对齐做准备）🟡】
        7. 代号符号化：给判定为推动剧情的核心角色起个简短自然的代号（如已知真名用真名，无真名用自然代号）。**极其重要：在 step3 中提及他们时，必须使用方括号包裹代号（例如：[红衣女人]、[李雷]）**。背景路人甲无需括号。
        8. 新面孔建档：只记录【当前片段内】核心角色的客观视觉特征，不要尝试判断他是否与前情提要中某人是同一人——跨片段身份对齐由后续独立流程负责。如果本片段没有需要登记的核心面孔，`new_characters_in_chunk` 保持为空列表。视觉档案中同样适用【不可辨识】原则——看不清的特征不要瞎编。

        【🟢 时间与格式强制约束（绝对服从）】
        9. 时间轴切分：事件的 `start_time`、`end_time` 以及角色建档的 `anchor_timestamp` 必须严格从以下列表中选取（禁止自我捏造）：
        可用时间戳白名单：{timestamps_str}

        请严格输出 JSON，禁止用 markdown 代码块包裹。"""


